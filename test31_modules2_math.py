import math

print(math.ceil(10.21))
print(math.fabs(-19.0077))

# Logarithmic and expo.. func.
print(math.exp(5))      #e**5
print(math.expm1(2))    #(e**2)-1
print(math.log(2, 10))  #log2/log10 --> log2 base 10

# Trigonometric Func..
print(math.acos(0))       #arc cosine,take input of value and gives answer into radians, means acos(val) == radians
print(math.asin(1))       #arc sine
print(math.atan(5))       #arc tan
print(math.cos(1.57))       #cosine, take input of radians and gives answer into value, means cos(radians) == value

# Angular, Hyperbolic, Func..
print(math.degrees(1))      # converts radians to degrees
print(math.radians(57.29))     # converts degrees to radians
print(math.pi)
print(math.e)

